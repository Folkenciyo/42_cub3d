#include "cub3d.h"

void	jorgito(char **line, int *flag)
{
	while (**line == ' ' || (**line >= 9 && **line <= 13)
		|| **line == 'C' || **line == 'F')
	{
		if (**line == 'C' || **line == 'F')
			(*flag)++;
		(*line)++;
	}
}

